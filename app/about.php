							<p>From Central Penn Parent: Capabilities, Winter 2009, “Caitlin’s Smiles: Big Smiles for Little Kids,” by Robin Passante.</p>
							<p>Cheryl Hornung was still wracked with grief over the loss of her 7-year-old daughter, Caitlin, when she made a fateful call to Penn State Hershey Children’s Hospital in December of 2000. Caitlin had died of cancer in October of that year, just before her 8th birthday. Her mother, a bargain shopper who snatches something up when she sees a good deal, already had purchased her birthday and Christmas gifts. “I called Penn State Children’s Hospital and asked if I could bring in all her presents,” says Hornung. “And I saw what a difference that made for the children.” Bringing smiles to sick kids gave Hornung a much-needed smile of her own.</p>
							<p>It also gave her a mission. Three years later, Hornung started Caitlin’s Smiles, a nonprofit organization that supplies craft kits, art supplies, and gift bags to kids with chronic and life-threatening illnesses. It honors the legacy of a little girl who loved to do arts and crafts and then give them all away. “She used to spend all her time coloring pictures and giving them to all the nurses that came in the room,” Hornung says of Caitlin, who was diagnosed with a brain tumor in 1997 at the age of 4. More surgeries and hospital stays followed, and Hornung and her husband made sure Caitlin always had her backpack of craft supplies with her to keep her distracted and happy. “When she was an outpatient, she’d pass out smiley-face cookies and whatever crafts she had that day to the other kids, doctors, and nurses,” Hornung recalls. “We found with her that healing was as much mental as it was physical.”</p>
							<p>When Caitlin lost her battle, it was Hornung who needed to heal. She volunteered for the organizations that had helped their family during Caitlin’s ordeal. But she kept thinking back to her daughter’s backpack of arts and crafts. Finally, in 2004, she decided to take the plunge. Progressive Education of Children in the Arts Network offered to help her start the nonprofit Caitlin’s Smiles. When she stopped by her church, First Zion Lutheran Church in Dauphin, she was given the keys to an empty office in the church building to use as Caitlin’s Smiles headquarters. Hornung’s little endeavor quickly gained steam, and she cultivated a cadre of volunteers to help her buy, pack, and deliver several different kinds of goody bags. The organization relies on donations of money and supplies, fundraisers, the occasional grant, and a lot of volunteers. “I am shocked daily,” she says of the outlet her organization gives to those who need to spread smiles, not just receive one. “I saw my clients as just being the kids and families in the hospital. And now I see how important it is for people to get hooked up to a good cause and be able to give back.” In February 2009, Caitlin’s Smiles moved its headquarters to downtown Harrisburg because it had long outgrown its space at the church. Trying to make rent payments — along with meeting every request that comes in — can be stressful, but Hornung says it’s comforting to stay so connected to Caitlin. “Most of the time I really feel her presence up there guiding me,” Hornung says. “I know she’d say, ‘Way to go, Mom. I love this!’”</p>
