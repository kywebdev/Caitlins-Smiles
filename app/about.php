							<h2>This is Caitlin's story.</h2>
							<p>Caitlin Mary Hornung’s favorite colors were yellow and pink. She loved swinging and taking walks and playing outside. She loved to dance and sing along to Disney songs, play dress-up, and take care of her baby sister, Danielle. One of her greatest joys came from creating art. In 1997, at the age of four, Caitlin was diagnosed with a malignant brain tumor. She spent the next three-and-a-half years battling for her life. Caitlin underwent surgeries, chemotherapy, a stem cell transplant, radiation treatments, hundreds of blood transfusions, two long stays in the Intensive Care Unit, and was treated at four different medical facilities. Several times her cancer appeared to have been in remission, but it kept coming back. Despite the tiring treatments and long hospital stays, Caitlin managed to keep a positive attitude by finding joy in creating art. She could always be found with a backpack full of arts and crafts supplies, coloring pictures for friends, family, and hospital staff. In October of 2000, shortly before her eighth birthday, Caitlin lost her battle with cancer. What friends and family learned from Caitlin was that healing is as much a mental process as it is a physical one. That is why in March of 2004, Cheryl Hornung, Caitlin’s mother, began Caitlin’s Smiles. Though Caitlin passed on in 2000, her smiling spirit is kept alive through the work of Caitlin’s Smiles<sup>™</sup>.</p>
