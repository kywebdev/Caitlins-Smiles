								<p>Caitlin’s Smiles returns to Elizabethtown College on Monday, November 12, from 6 to 8 pm, for an evening of making smiles.</p>
								<p>Students and community members are invited to volunteer for Caitlin’s Smiles at the Koons Activity Venue in Brossman Commons on the Elizabethtown College campus. After viewing a brief video on Caitlin's Smiles, all participants will be instructed in creating greeting cards, decorating gift bags and assembling craft kits to “spread smiles” to children with chronic or life-threatening conditions in hospitals in the Mid-Atlantic region. These goodies are included in the “Bags of Smiles” sent by Caitlin’s Smiles to over 100 health care facilities. In 2017, over 14,000 Bags of Smiles were distributed in Pennsylvania and nine other states.</p>
								<p>The Circle K International club of Elizabethtown College is partnering with Caitlin’s Smiles for this event. People wishing to volunteer at the Elizabethtown College event should register online through the events calendar on Caitlin’s Smiles’ website at <a href="https://caitlins-smiles.org" target="_blank">www.caitlins-smiles.org</a>.</p>
								<p>The cost of admission is one can of Play-Doh<sup>®</sup> or one package of mini Play-Doh<sup>®</sup>. Parking is available on campus near the Leffler Chapel, off Alpha Drive, or behind Brossman Commons, off Cedar Street. For GPS, use 1 Alpha Drive, Elizabethtown College, Elizabethtown, PA 17022.</p>
								<p>Caitlin’s Smiles is a nonprofit organization based in Harrisburg, Pennsylvania, that provides arts & crafts activities to children facing chronic or life-threatening conditions. With the help of donors and volunteers, Caitlin’s Smiles reaches children in Pennsylvania, Ohio, Maryland, Delaware, New York, New Jersey, West Virginia, Virginia and the District of Columbia.</p>
								<p>For more information about Caitlin’s Smiles and to find out how you can get involved, visit the website at <a href="https://caitlins-smiles.org" target="_blank">www.caitlins-smiles.org</a>.</p>
								<p>Circle K is a service organization that is the college-level group of the Kiwanis family. Elizabethtown College's chapter meets every week. For times and locations, email <a href="mailto:circlek@etown.edu">circlek@etown.edu</a>.</p>
